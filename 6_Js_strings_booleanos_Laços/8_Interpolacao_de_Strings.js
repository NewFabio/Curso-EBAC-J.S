let nome = 'Fabio';
let idade = '25';

//Isso é interpolação
let mensagem = `Meu nome é ${nome} e eu tenho ${idade} anos.`
console.log(mensagem)

//Em programação, as strings representam sequências de caracteres, como texto, e são frequentemente usadas para armazenar informações legíveis por humanos.﻿

//Métodos de String:

//.length: Retorna o comprimento da string.

//.toUpperCase(): Converte a string para letras maiúsculas.

//.toLowerCase(): Converte a string para letras minúsculas.

//.indexOf(): Retorna a posição da primeira ocorrência de um determinado texto dentro da string.

//.substring(): Extrai uma parte da string, especificando o índice de início e fim.

//.replace(): Substitui uma parte da string por outra.

//Interpolação de Strings: Usando o operador ${} dentro de uma string delimitada por crases (``), podemos inserir valores de variáveis ou expressões JavaScript diretamente na string.
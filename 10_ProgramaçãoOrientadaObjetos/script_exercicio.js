function soma(num1, num2) {
    return num1 + num2;
}
console.log('O resultado da soma é:', soma(10,5));

function subtrair(num1, num2) {
    return num1 - num2;
}
const result = subtrair(5,3)
console.log('A subtração foi:', result);

function multi(num1, num2) {
    return num1 * num2;
}

const resultado = multi(5,5);
console.log('O resultado da multiplicação é:', resultado)

function divisao(num1, num2) {
    return num1 / num2;
}

const re = divisao(10,2);
console.log('O resultado da divisão é:', re)